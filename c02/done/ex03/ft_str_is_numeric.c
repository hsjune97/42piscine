int		ft_str_is_numeric(char *str)
{
	int cur;

	cur = 0;
	while (str[cur] != 0)
	{
		if ('0' <= str[cur] && str[cur] <= '9')
		{
			cur++;
			continue;
		}
		else
		{
			return (0);
		}
	}
	return (1);
}
